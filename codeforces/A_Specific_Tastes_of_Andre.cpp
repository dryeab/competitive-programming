/*
    https://codeforces.com/contest/1438/problem/A
    Time: O(n)
    Space: O(1)
*/

#include <bits/stdc++.h>

using namespace std;

int main()
{
    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        for (int i = 0; i < n; ++i)
            printf("1 ");

        printf("\n");
    }
}